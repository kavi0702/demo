package Advancejavatopic;

public class Javadowhile {

	public static void main(String[] args) {
		int a=400;
		do
		{
			System.out.println(a);
			a++;
		}
		while(a<=500);
		
		System.out.println("-----------------decrement------------");
		
		int b=250;
		do
		{
			System.out.println(b);
			b--;
			
		}
		while(b>=150);
		

	}

}
